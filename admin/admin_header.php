$labels = [];
$monthlyRevenue = [];
$monthlyOrders = [];

for ($i = 0; $i < 6; $i++) {
    $month = date("Y-m", strtotime("-$i months"));
    $label = date("F", strtotime($month)); // Month name like "January"
    $labels[] = $label;

    // Revenue for the month
    $revQuery = mysqli_query($conn, "SELECT SUM(order_amount) as total FROM orders WHERE DATE_FORMAT(date, '%Y-%m') = '$month'");
    $revRow = mysqli_fetch_assoc($revQuery);
    $monthlyRevenue[] = $revRow['total'] ?? 0;

    // Orders for the month
    $orderQuery = mysqli_query($conn, "SELECT COUNT(*) as count FROM orders WHERE DATE_FORMAT(date, '%Y-%m') = '$month'");
    $orderRow = mysqli_fetch_assoc($orderQuery);
    $monthlyOrders[] = $orderRow['count'] ?? 0;
}
// Reverse arrays so months appear in correct order
$labels = array_reverse($labels);
$monthlyRevenue = array_reverse($monthlyRevenue);
$monthlyOrders = array_reverse($monthlyOrders);
